package chapter4;

public class Checkerboard {
    public static void main(String[] args) {
         int counter = 1;

        while (counter <= 4) {
            int row = 1;

            while (row <= 8) {
                System.out.print("*");
                row++;
            }
            System.out.println();

            System.out.println("");


        }
    }
}
